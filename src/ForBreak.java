public class ForBreak { //start of class 

    public static void main(String[] args) { // start of main method 

        for (int i = 1; i <= 50; i++) {
            if (i == 10)
            break;
            System.out.println(i + " ");

        }// end of looping
        System.out.println();
        System.out.println("exiting out of loop");
    } //end of main method
} // end of class
